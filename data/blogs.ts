export const getBlogBySlug = (slug: string, lang: string = 'fr'): BlogPost | undefined => {
  const post = BLOGS.find(b => b.slug === slug);
  if (!post) return undefined;
  
  if (lang === 'fr' || !post.translations?.[lang]) return post;
  
  return {
    ...post,
    title: post.translations[lang].title,
    excerpt: post.translations[lang].excerpt,
    content: post.translations[lang].content,
    tags: post.translations[lang].tags
  };
};

export const getLocalizedBlogs = (lang: string = 'fr'): BlogPost[] => {
  return BLOGS.map(post => {
    if (lang === 'fr' || !post.translations?.[lang]) return post;
    return {
      ...post,
      title: post.translations[lang].title,
      excerpt: post.translations[lang].excerpt,
      content: post.translations[lang].content,
      tags: post.translations[lang].tags
    };
  });
};
